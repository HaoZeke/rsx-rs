/// Bayes Factor for association in a 2x2 contingency table.
/// BF > 1: evidence for association. BF > 10: strong evidence.
/// Uses Beta-Binomial marginal likelihoods with uniform Beta(1,1) priors.
/// H1: separate proportions per group. H0: shared proportion.
pub fn bayes_factor_2x2(n_g1: u32, n_g2: u32, total_g1: u32, total_g2: u32) -> f64 {
    // H1 (association): p_g1, p_g2 independent, each Beta(1,1)
    // Marginal = B(k+1, n-k+1) / B(1,1) for each group
    let log_h1 = log_beta_binom(n_g1, total_g1) + log_beta_binom(n_g2, total_g2);

    // H0 (independence): single shared p ~ Beta(1,1)
    let log_h0 = log_beta_binom(n_g1 + n_g2, total_g1 + total_g2);

    (log_h1 - log_h0).exp()
}

/// Log marginal likelihood for Binomial(k|n) with Beta(1,1) prior.
fn log_beta_binom(k: u32, n: u32) -> f64 {
    libm::lgamma((k + 1) as f64) + libm::lgamma((n - k + 1) as f64) - libm::lgamma((n + 2) as f64)
}

/// Posterior probability that a marker is sex-linked (empirical Bayes).
/// Uses a Beta-Binomial conjugate model.
/// pi: prior probability of sex-linkage (estimated from data or set to 0.01).
/// p_sex: assumed frequency in the linked sex (e.g., 0.9).
pub fn posterior_sex_linked(
    n_g1: u32,
    n_g2: u32,
    total_g1: u32,
    total_g2: u32,
    pi: f64,
    p_sex: f64,
) -> f64 {
    let ll_linked = binom_logpmf(n_g1, total_g1, p_sex) + binom_logpmf(n_g2, total_g2, 1.0 - p_sex);
    let ll_null = binom_logpmf(n_g1, total_g1, 0.5) + binom_logpmf(n_g2, total_g2, 0.5);

    let log_odds = ll_linked - ll_null + (pi / (1.0 - pi)).ln();

    // Posterior = 1 / (1 + exp(-log_odds))
    if log_odds > 20.0 {
        1.0 // overflow guard
    } else if log_odds < -20.0 {
        0.0
    } else {
        1.0 / (1.0 + (-log_odds).exp())
    }
}

fn binom_logpmf(k: u32, n: u32, p: f64) -> f64 {
    if p <= 0.0 || p >= 1.0 {
        if (p <= 0.0 && k == 0) || (p >= 1.0 && k == n) {
            return 0.0;
        }
        return f64::NEG_INFINITY;
    }
    lfact(n) - lfact(k) - lfact(n - k) + k as f64 * p.ln() + (n - k) as f64 * (1.0 - p).ln()
}

/// Empirical Bayes EM: estimate pi (fraction of sex-linked markers) from data.
/// Returns (pi, posteriors) after convergence.
/// group_counts: Vec of (n_g1, n_g2) for each marker.
pub fn empirical_bayes_em(
    group_counts: &[(u32, u32)],
    total_g1: u32,
    total_g2: u32,
    p_sex: f64,
    max_iter: usize,
) -> (f64, Vec<f64>) {
    let m = group_counts.len();
    let mut pi = 0.01f64; // initial estimate
    let mut posteriors = vec![0.0f64; m];

    for _iter in 0..max_iter {
        // E-step: compute posteriors
        for (i, &(n_g1, n_g2)) in group_counts.iter().enumerate() {
            posteriors[i] = posterior_sex_linked(n_g1, n_g2, total_g1, total_g2, pi, p_sex);
        }

        // M-step: update pi
        let new_pi: f64 = posteriors.iter().sum::<f64>() / m as f64;
        if (new_pi - pi).abs() < 1e-8 {
            pi = new_pi;
            break;
        }
        pi = new_pi;
    }

    // Final E-step with converged pi
    for (i, &(n_g1, n_g2)) in group_counts.iter().enumerate() {
        posteriors[i] = posterior_sex_linked(n_g1, n_g2, total_g1, total_g2, pi, p_sex);
    }

    (pi, posteriors)
}


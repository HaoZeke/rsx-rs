    #[test]
    fn test_bh_fdr_basic() {
        let pvals = vec![0.01, 0.04, 0.03, 0.20, 0.50];
        let q = benjamini_hochberg(&pvals);
        assert_eq!(q.len(), 5);
        // All q-values should be >= corresponding p-values
        for (p, q) in pvals.iter().zip(q.iter()) {
            assert!(*q >= *p, "q={q} < p={p}");
        }
        // q-values should be <= 1.0
        for &qi in &q {
            assert!(qi <= 1.0);
        }
    }


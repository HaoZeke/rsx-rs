/// G-test (log-likelihood ratio) for 2x2 table.
/// Better asymptotic properties than chi-squared.
pub fn g_test(n_g1: u32, n_g2: u32, total_g1: u32, total_g2: u32) -> f64 {
    let a = n_g1 as f64;
    let b = (total_g1 - n_g1) as f64;
    let c = n_g2 as f64;
    let d = (total_g2 - n_g2) as f64;
    let n = (total_g1 + total_g2) as f64;

    let mut g = 0.0f64;
    let expected = |_obs: f64, col_total: f64, row_total: f64| -> f64 { row_total * col_total / n };

    let row1 = a + c;
    let row2 = b + d;
    let col1 = a + b;
    let col2 = c + d;

    for &(obs, rt, ct) in &[
        (a, row1, col1),
        (b, row2, col1),
        (c, row1, col2),
        (d, row2, col2),
    ] {
        if obs > 0.0 {
            let exp = expected(obs, ct, rt);
            if exp > 0.0 {
                g += obs * (obs / exp).ln();
            }
        }
    }
    g *= 2.0;

    // P-value: same chi-squared CDF with df=1
    if g.is_nan() || g <= 0.0 {
        1.0
    } else {
        chi_squared_p(g).clamp(1e-16, 1.0)
    }
}

/// Fisher's exact test for 2x2 table (one-sided, more extreme).
/// Uses hypergeometric probability. Better than chi-squared for small n.
pub fn fisher_exact(n_g1: u32, n_g2: u32, total_g1: u32, total_g2: u32) -> f64 {
    let a = n_g1;
    let b = total_g1 - n_g1;
    let c = n_g2;
    let d = total_g2 - n_g2;
    let n = total_g1 + total_g2;
    let row1 = a + c;
    let col1 = a + b;

    // P(table) = C(row1,a) * C(row2,b) / C(n, col1)
    // Sum probabilities of all tables as or more extreme
    let log_p_observed = log_hypergeometric(a, b, c, d, n, row1, col1);

    let min_a = col1.saturating_sub(n - row1);
    let max_a = row1.min(col1);

    // Collect log-probs of tables as or more extreme (density tail).
    // Use log-sum-exp for numerical stability when individual probs underflow.
    let mut tail_logs: Vec<f64> = Vec::new();
    for a_i in min_a..=max_a {
        let b_i = col1 - a_i;
        let c_i = row1 - a_i;
        let d_i = (n - row1) - b_i;
        let log_p = log_hypergeometric(a_i, b_i, c_i, d_i, n, row1, col1);
        // Include if <= observed (within tiny fp tolerance for lgamma error)
        if log_p <= log_p_observed + 1e-9 {
            tail_logs.push(log_p);
        }
    }

    let p_sum = if tail_logs.is_empty() {
        0.0
    } else {
        // logsumexp then exp
        let max_log = tail_logs.iter().cloned().fold(f64::NEG_INFINITY, f64::max);
        let sum_rel: f64 = tail_logs.iter().map(|&l| (l - max_log).exp()).sum();
        (max_log + sum_rel.ln()).exp()
    };

    p_sum.clamp(1e-16, 1.0)
}

fn log_hypergeometric(a: u32, b: u32, c: u32, d: u32, n: u32, row1: u32, col1: u32) -> f64 {
    let col2 = n - col1;
    let row2 = n - row1;
    lfact(row1) + lfact(row2) + lfact(col1) + lfact(col2)
        - lfact(n)
        - lfact(a)
        - lfact(b)
        - lfact(c)
        - lfact(d)
}

fn lfact(n: u32) -> f64 {
    libm::lgamma((n + 1) as f64)
}

